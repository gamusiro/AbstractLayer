#pragma once
#include "IF_Object.h"

class IScene
{
public:
	//**************************************************
	/// \brief デストラクタ
	/// 
	/// \return なし
	//**************************************************
	virtual ~IScene() {}

public:
	//**************************************************
	/// \brief 初期化処理
	/// 
	/// \return なし
	//**************************************************
	virtual void Init() {};
	
	//**************************************************
	/// \brief 終了処理
	/// 
	/// \return なし
	//**************************************************
	virtual void Uninit() {};

	//**************************************************
	/// \brief 更新処理
	/// 
	/// \return なし
	//**************************************************
	virtual void Update() = 0;

	//**************************************************
	/// \brief 描画処理
	/// 
	/// \return なし
	//**************************************************
	virtual void Draw() = 0;
};

